import { useState } from "react";
import { Link } from "react-router-dom";
import { MapPin, FileText, Activity, Info, Send, Link as LinkIcon, Download, StickyNote, ExternalLink, MessageSquare, Phone, FileCheck, FileSignature, Handshake, Archive, XCircle, RotateCcw, HelpCircle, Bell, Sparkles, ArrowRight } from "lucide-react";
import { Button } from "@/components/ui/button";
import { Textarea } from "@/components/ui/textarea";
import {
  Dialog, DialogContent, DialogHeader, DialogTitle, DialogDescription, DialogFooter,
} from "@/components/ui/dialog";
import { useToast } from "@/hooks/use-toast";
import { cn } from "@/lib/utils";
import type { Relationship } from "@/features/matches/hooks/useUnifiedRelationships";
import { currency, scoreDotClass } from "../helpers";
import { WhyThisMatched } from "./WhyThisMatched";
import { MatchBreakdownChart } from "./MatchBreakdownChart";
import { AgentCommsCard } from "./AgentCommsCard";
import { LifecycleTracker } from "./LifecycleTracker";
import {
  financialMetrics,
  rankExplanation,
  UI_STATUS_CLASS,
  UI_STATUS_LABEL,
} from "./inboxHelpers";
import { useMatchActions } from "./useMatchActions";
import { useMatchLocalState } from "./useMatchLocalState";
import { propertyImage } from "./propertyImage";
import { getClientAccent } from "@/features/matches/lib/clientAccent";
import { ClientLeadLine } from "@/features/matches/components/shared/ClientLeadLine";

interface Props {
  rel: Relationship;
  rank?: number | null;
  totalInScope?: number;
}

const KEY_METRIC_KEYS = ["noi", "cap", "coc", "dscr", "occupancy", "equity", "loan", "cashflow"];

export function PropertyReviewPanel({ rel, rank, totalInScope }: Props) {
  const allMetrics = financialMetrics(rel);
  const keyMetrics = KEY_METRIC_KEYS
    .map((k) => allMetrics.find((m) => m.key === k))
    .filter(Boolean) as ReturnType<typeof financialMetrics>;

  const { status, primary, secondary, handle, busy } = useMatchActions(rel, {
    onSendToClient: () => setShareOpen(true),
  });

  const { state, update } = useMatchLocalState(rel.matchId);
  const { toast } = useToast();
  const [shareOpen, setShareOpen] = useState(false);
  const [note, setNote] = useState(state.agentNote);

  const shareUrl = `${window.location.origin}/share/match/${rel.matchId}`;

  function confirmSend() {
    update({ sentToClientAt: new Date().toISOString() });
    setShareOpen(false);
    toast({ title: "Sent to client", description: rel.clientName ? `Notified ${rel.clientName}.` : undefined });
  }

  function copyLink() {
    navigator.clipboard.writeText(shareUrl).then(
      () => toast({ title: "Link copied" }),
      () => toast({ title: "Copy failed", variant: "destructive" }),
    );
  }

  function downloadOnePager() {
    const lines = [
      `Property: ${rel.propertyName}`,
      `Location: ${[rel.propertyCity, rel.propertyState].filter(Boolean).join(", ") || "—"}`,
      `Match score: ${Math.round(rel.score)}/100`,
      "",
      "Financials",
      ...allMetrics.map((m) => `  ${m.label}: ${m.value}${m.estimated ? "  (est.)" : ""}`),
      "",
      "Generated by 1031ExchangeUp",
    ].join("\n");
    const blob = new Blob([lines], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `${rel.propertyName.replace(/\s+/g, "_")}_one-pager.txt`;
    a.click();
    URL.revokeObjectURL(url);
    toast({ title: "One-pager downloaded" });
  }

  function saveNote() {
    update({ agentNote: note });
    toast({ title: "Note saved" });
  }

  const accent = getClientAccent(rel.clientId);

  return (
    <div className="flex h-full min-h-0 w-full min-w-0 flex-col overflow-hidden rounded-xl border bg-card">
      <div className="min-h-0 flex-1 overflow-y-auto overscroll-contain">
        {/* Client identity strip */}
        <div
          className={cn(
            "flex flex-wrap items-center gap-2 border-b border-border border-l-[4px] px-5 py-2.5",
            accent.borderLeft,
            accent.soft,
          )}
        >
          <ClientLeadLine
            clientId={rel.clientId}
            clientName={rel.clientName}
            relinquishedLabel={rel.relinquishedLabel}
            size="md"
            pill
          />
          <span className="hidden text-xs text-muted-foreground sm:inline">
            Trading out — finding replacement property
          </span>
          <Button asChild variant="ghost" size="sm" className="ml-auto h-7 px-2 text-xs">
            <Link to={`/agent/workspace/${rel.buyerExchangeId}`}>
              Open workspace <ExternalLink className="ml-1 h-3 w-3" />
            </Link>
          </Button>
        </div>

        {/* Hero image */}
        <div className="relative h-56 w-full overflow-hidden bg-muted">
          <img
            src={propertyImage(rel.propertyImageUrl, rel.id)}
            alt=""
            className="h-full w-full object-cover"
          />
          <span
            className={cn(
              "absolute right-3 top-3 inline-flex max-w-[60%] items-center whitespace-nowrap rounded-full border bg-card/95 px-2.5 py-1 text-xs font-medium backdrop-blur",
              UI_STATUS_CLASS[status],
            )}
          >
            {UI_STATUS_LABEL[status]}
          </span>
        </div>

        {/* Header */}
        <div className="border-b border-border px-5 py-4">
          <div className="flex flex-wrap items-start justify-between gap-3">
            <div className="min-w-0 flex-1">
              <h2 className="truncate text-xl font-semibold text-foreground">
                {rel.propertyName}
              </h2>
              <p className="mt-0.5 flex items-center gap-1 truncate text-sm text-muted-foreground">
                <MapPin className="h-3.5 w-3.5 shrink-0" />
                <span className="truncate">
                  {[rel.propertyCity, rel.propertyState].filter(Boolean).join(", ") || "—"}
                </span>
              </p>
              {rank != null && totalInScope ? (
                <p className="mt-1 text-[11px] font-medium text-foreground/70">
                  <span className="rounded bg-muted px-1.5 py-0.5 font-bold">#{rank}</span>
                  <span className="ml-1.5">of {totalInScope} matches</span>
                </p>
              ) : null}
            </div>

            <div className="flex shrink-0 items-center gap-3">
              <div className="text-right">
                <p className="text-lg font-semibold leading-none text-foreground">
                  {currency(rel.askingPrice)}
                </p>
                {rel.capRate != null && (
                  <p className="mt-1 text-[11px] text-muted-foreground">
                    {rel.capRate.toFixed(2)}% cap
                  </p>
                )}
              </div>
              <div className="flex flex-col items-center">
                <span
                  className={cn(
                    "flex h-9 min-w-9 items-center justify-center rounded-full px-2 text-sm font-bold text-white",
                    scoreDotClass(rel.score),
                  )}
                >
                  {Math.round(rel.score)}
                </span>
                <span className="mt-0.5 text-[10px] text-muted-foreground">score</span>
              </div>
            </div>
          </div>

        </div>

        {/* Action Center */}
        <ActionCenter
          rel={rel}
          status={status}
          primary={primary}
          secondary={secondary}
          handle={handle}
          busy={busy}
          accentBorderLeft={accent.borderLeft}
        />


        {/* Lifecycle tracker */}
        <div className="border-b border-border px-5 py-3">
          <LifecycleTracker status={status} variant="compact" />
        </div>

        {/* Key metrics strip */}
        <div className="border-b border-border px-5 py-3">
          <h3 className="mb-2 text-xs font-semibold uppercase tracking-wide text-muted-foreground">
            Key metrics
          </h3>
          <div className="grid grid-cols-2 gap-2 sm:grid-cols-4 2xl:grid-cols-8">
            {keyMetrics.map((m) => (
              <div key={m.key} className="rounded-lg border bg-background px-2.5 py-2">
                <div className="flex items-center gap-1 text-[10px] font-medium uppercase tracking-wide text-muted-foreground">
                  <span className="truncate">{m.label}</span>
                  {m.estimated && <Info className="h-2.5 w-2.5 shrink-0" aria-label="Estimated" />}
                </div>
                <p className="mt-0.5 truncate text-sm font-semibold text-foreground">{m.value}</p>
              </div>
            ))}
          </div>
        </div>

        {/* Why this matched + rank explanation */}
        <section className="space-y-4 border-b border-border px-5 py-5">
          <div>
            <h3 className="mb-2 text-sm font-semibold text-foreground">Why this matched</h3>
            {rank != null && (
              <div className="mb-3 rounded-lg border bg-primary/5 p-3 text-sm text-foreground/80">
                {rankExplanation(rel, rank)}
              </div>
            )}
            <WhyThisMatched rel={rel} />
          </div>
        </section>

        {/* Match breakdown */}
        <section className="border-b border-border px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Match breakdown</h3>
          <MatchBreakdownChart rel={rel} />
        </section>

        {/* Full financials */}
        <section className="border-b border-border px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Financials</h3>
          <FinancialGrid metrics={allMetrics} />
        </section>

        {/* Share with client */}
        <section className="border-b border-border px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Share with client</h3>
          <div className="flex flex-wrap gap-2">
            <Button size="sm" onClick={() => setShareOpen(true)}>
              <Send className="mr-1.5 h-3.5 w-3.5" /> Send to client
              {state.sentToClientAt && <span className="ml-2 text-[10px] opacity-80">Sent</span>}
            </Button>
            <Button variant="outline" size="sm" onClick={copyLink}>
              <LinkIcon className="mr-1.5 h-3.5 w-3.5" /> Copy client link
            </Button>
            <Button variant="outline" size="sm" onClick={downloadOnePager}>
              <Download className="mr-1.5 h-3.5 w-3.5" /> Download one-pager
            </Button>
          </div>
          <div className="mt-4">
            <label className="mb-1.5 flex items-center gap-1.5 text-xs font-medium text-foreground">
              <StickyNote className="h-3.5 w-3.5" /> Internal note (not shared with client)
            </label>
            <Textarea
              value={note}
              onChange={(e) => setNote(e.target.value)}
              placeholder="Add context for yourself…"
              rows={3}
              className="text-sm"
            />
            <Button
              size="sm"
              variant="outline"
              className="mt-2"
              onClick={saveNote}
              disabled={note === state.agentNote}
            >
              Save note
            </Button>
          </div>
        </section>

        {/* Conversation with counterparty agent */}
        <section className="border-b border-border px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Conversation</h3>
          <AgentCommsCard rel={rel} />
        </section>

        {/* Activity */}
        <section className="border-b border-border px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Activity</h3>
          <ActivityTimeline rel={rel} />
        </section>

        {/* Documents */}
        <section className="px-5 py-5">
          <h3 className="mb-3 text-sm font-semibold text-foreground">Documents</h3>
          <div className="flex flex-col items-center justify-center rounded-xl border border-dashed bg-muted/30 p-10 text-center">
            <FileText className="mb-3 h-10 w-10 text-muted-foreground/40" />
            <p className="text-sm font-medium text-foreground">No documents shared yet</p>
            <p className="mt-1 max-w-sm text-xs text-muted-foreground">
              Documents the other agent shares (OM, T-12, rent roll) will appear here.
            </p>
            <Button disabled size="sm" variant="outline" className="mt-4">
              Request documents
            </Button>
          </div>
        </section>
      </div>

      <Dialog open={shareOpen} onOpenChange={setShareOpen}>
        <DialogContent>
          <DialogHeader>
            <DialogTitle>Send to client?</DialogTitle>
            <DialogDescription>
              {rel.clientName ? (
                <>We'll share <strong>{rel.propertyName}</strong> with <strong>{rel.clientName}</strong> and notify them by email.</>
              ) : (
                <>We'll share <strong>{rel.propertyName}</strong> with your client and notify them by email.</>
              )}
            </DialogDescription>
          </DialogHeader>
          <DialogFooter>
            <Button variant="outline" onClick={() => setShareOpen(false)}>Cancel</Button>
            <Button onClick={confirmSend}>Send</Button>
          </DialogFooter>
        </DialogContent>
      </Dialog>
    </div>
  );
}

function FinancialGrid({ metrics }: { metrics: ReturnType<typeof financialMetrics> }) {
  return (
    <div className="grid grid-cols-2 gap-3 sm:grid-cols-3">
      {metrics.map((m) => (
        <div key={m.key} className="rounded-lg border bg-background p-3">
          <div className="flex items-center gap-1 text-[10px] font-medium uppercase tracking-wide text-muted-foreground">
            {m.label}
            {m.estimated && <Info className="h-3 w-3" aria-label="Estimated value" />}
          </div>
          <p className="mt-1 text-base font-semibold text-foreground">{m.value}</p>
        </div>
      ))}
    </div>
  );
}

function ActivityTimeline({ rel }: { rel: Relationship }) {
  const events: Array<{ ts: string; label: string }> = [];
  events.push({ ts: rel.lastActivityAt, label: "Most recent activity" });
  if (rel.acceptedAt) events.push({ ts: rel.acceptedAt, label: "Connection accepted" });
  if (rel.underContractAt) events.push({ ts: rel.underContractAt, label: "Under contract" });
  if (rel.closedAt) events.push({ ts: rel.closedAt, label: "Deal closed" });
  events.push({ ts: rel.lastActivityAt, label: "Match created" });

  return (
    <ol className="relative space-y-3 border-l border-border pl-4">
      {events.map((e, i) => (
        <li key={i} className="relative">
          <span className="absolute -left-[21px] top-1.5 h-2 w-2 rounded-full bg-primary" />
          <p className="text-sm font-medium text-foreground">{e.label}</p>
          <p className="text-xs text-muted-foreground">
            {new Date(e.ts).toLocaleString()}
          </p>
        </li>
      ))}
      {events.length === 1 && (
        <p className="pt-2 text-xs text-muted-foreground">
          <Activity className="mr-1 inline h-3 w-3" /> More activity will appear as the deal progresses.
        </p>
      )}
    </ol>
  );
}

// ── Action Center ────────────────────────────────────────────────

const ACTION_ICONS: Record<string, typeof Send> = {
  send_to_client: Send,
  request_seller_details: HelpCircle,
  not_a_fit: XCircle,
  mark_interested: Sparkles,
  follow_up_client: Bell,
  client_passed: XCircle,
  request_agent_intro: Handshake,
  send_client_questions: HelpCircle,
  open_conversation: MessageSquare,
  schedule_call: Phone,
  request_documents: FileText,
  start_reviewing_docs: FileCheck,
  mark_loi_sent: FileSignature,
  mark_under_contract: FileSignature,
  archive: Archive,
  reactivate: RotateCcw,
};

const STATUS_HINTS: Record<string, string> = {
  new: "Fresh match — share it with your client to gauge interest.",
  sent_to_client: "Waiting on your client. Nudge them or log their response.",
  client_interested: "Client is in. Request an intro to the listing agent.",
  agent_connected: "You're connected — keep the conversation moving.",
  reviewing_docs: "Diligence underway. Move to LOI when ready.",
  loi: "Offer on the table. Update once it goes under contract.",
  under_contract: "Under contract. Track to close in the conversation.",
  closed: "Deal closed. Nice work.",
  archived: "Archived. Reactivate to resume work on this match.",
};

interface ActionCenterProps {
  rel: Relationship;
  status: keyof typeof STATUS_HINTS;
  primary: { id: string; label: string } | null;
  secondary: Array<{ id: string; label: string; tone?: "primary" | "secondary" | "destructive" }>;
  handle: (id: string, label: string) => void;
  busy: string | null;
  accentBorderLeft: string;
}

function ActionCenter({ status, primary, secondary, handle, busy, accentBorderLeft }: ActionCenterProps) {
  const PrimaryIcon = primary ? ACTION_ICONS[primary.id] ?? ArrowRight : null;
  const constructive = secondary.filter((a) => a.tone !== "destructive");
  const destructive = secondary.filter((a) => a.tone === "destructive");

  return (
    <div className="border-b border-border px-5 py-4">
      <div
        className={cn(
          "relative overflow-hidden rounded-xl border border-l-[4px] bg-gradient-to-br from-primary/[0.04] via-card to-card p-4 shadow-sm",
          accentBorderLeft,
        )}
      >
        <div className="absolute -right-16 -top-16 h-40 w-40 rounded-full bg-primary/5 blur-3xl" aria-hidden />

        <div className="relative flex flex-col gap-4 md:flex-row md:items-start md:justify-between">
          {/* Left: status + hint */}
          <div className="min-w-0 md:max-w-xs">
            <div className="flex items-center gap-2">
              <span
                className={cn(
                  "inline-flex items-center rounded-full border px-2 py-0.5 text-[10px] font-semibold uppercase tracking-wide",
                  UI_STATUS_CLASS[status as keyof typeof UI_STATUS_CLASS],
                )}
              >
                {UI_STATUS_LABEL[status as keyof typeof UI_STATUS_LABEL]}
              </span>
              <span className="text-[10px] font-semibold uppercase tracking-wide text-muted-foreground">
                Action Center
              </span>
            </div>
            <p className="mt-2 text-sm font-medium text-foreground/90">
              {STATUS_HINTS[status] ?? "Keep this deal moving."}
            </p>
          </div>

          {/* Right: actions */}
          <div className="flex min-w-0 flex-1 flex-col items-stretch gap-3 md:items-end">
            {primary ? (
              <Button
                size="lg"
                className="group h-11 w-full justify-center gap-2 px-5 text-sm font-semibold shadow-md md:w-auto"
                onClick={() => handle(primary.id, primary.label)}
                disabled={busy === primary.id}
              >
                {PrimaryIcon && <PrimaryIcon className="h-4 w-4" />}
                {primary.label}
                <ArrowRight className="h-4 w-4 opacity-70 transition-transform group-hover:translate-x-0.5" />
              </Button>
            ) : (
              <span className="text-xs text-muted-foreground">No further action required.</span>
            )}

            {(constructive.length > 0 || destructive.length > 0) && (
              <div className="flex w-full flex-wrap items-center gap-1.5 md:justify-end">
                {constructive.map((a) => {
                  const Icon = ACTION_ICONS[a.id];
                  return (
                    <button
                      key={a.id}
                      type="button"
                      onClick={() => handle(a.id, a.label)}
                      disabled={busy === a.id}
                      className="inline-flex items-center gap-1.5 rounded-full border border-border bg-card px-3 py-1.5 text-xs font-medium text-foreground/80 transition-colors hover:border-primary/40 hover:bg-primary/5 hover:text-foreground disabled:opacity-50"
                    >
                      {Icon && <Icon className="h-3.5 w-3.5" />}
                      {a.label}
                    </button>
                  );
                })}

                {destructive.length > 0 && constructive.length > 0 && (
                  <span className="mx-1 h-4 w-px bg-border" aria-hidden />
                )}

                {destructive.map((a) => {
                  const Icon = ACTION_ICONS[a.id];
                  return (
                    <button
                      key={a.id}
                      type="button"
                      onClick={() => handle(a.id, a.label)}
                      disabled={busy === a.id}
                      className="inline-flex items-center gap-1.5 rounded-full border border-transparent px-3 py-1.5 text-xs font-medium text-destructive/80 transition-colors hover:bg-destructive/10 hover:text-destructive disabled:opacity-50"
                    >
                      {Icon && <Icon className="h-3.5 w-3.5" />}
                      {a.label}
                    </button>
                  );
                })}
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}

